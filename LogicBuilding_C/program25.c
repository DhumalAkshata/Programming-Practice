//program to write 5 times Jay Ganesh on screen in Iteration Approach using for loop

#include<stdio.h>
int main()
{
    int iCnt = 0;
    //      1          2       3
    for(iCnt = 1; iCnt <= 5; iCnt++)
    {             //4
        printf("Jay Ganesh...");
    }
    return 0;
}

/*
1 : Loop counter initialization
2 : Loop terminating condition
3 : Loop counter displacement
4 : Loop body
*/